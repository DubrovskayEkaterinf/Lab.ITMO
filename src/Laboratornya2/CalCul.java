package Laboratornya2;

import java.util.Scanner;

public class CalCul {

    public static double addition(double num1, double num2) {
        return num1 + num2;
    }

    public static long addition(long num1, long num2) {
        return num1 + num2;
    }

    public static int addition(int num1, int num2) {
        return num1 + num2;
    }

    public static double subtraction(double num1, double num2) {
        return num1 - num2;
    }

    public static long subtraction(long num1, long num2) {
        return num1 - num2;
    }

    public static int subtraction(int num1, int num2) {
        return num1 - num2;
    }

    public static double multiplication(double num1, double num2) {
        return num1 * num2;
    }

    public static long multiplication(long num1, long num2) {
        return num1 * num2;
    }

    public static double multiplication(int num1, int num2) {
        return num1 * num2;
    }

    public static double division(double num1, double num2) {

        return num1 / num2;
    }

    public static double division(long num1, long num2) {
        return num1*1.0 / num2;
    }

    public static double division(int num1, int num2) {

        return num1*1.0 / num2;
    }
}





